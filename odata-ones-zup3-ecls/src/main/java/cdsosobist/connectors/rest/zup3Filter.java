package cdsosobist.connectors.rest;

public class zup3Filter {

    public String byName;
    public String byUid;
    public String byEmailAddress;

    public zup3Filter() {
    }

    public String toString() {
        return "YFilter{byName='" + this.byName + '\'' + ", byUid=" + this.byUid + ", byEmailAddress='" + this.byEmailAddress + '\'' + '}';
    }
}
